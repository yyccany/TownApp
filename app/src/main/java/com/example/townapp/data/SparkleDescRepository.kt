    /**
     * 根据物品名称获取闪光点文案
     */
    fun getSparkle(itemName: String): String? = sparkleMap[itemName]

    /**
     * 模糊匹配获取闪光点（用于名称不完全一致的情况）
     */
    fun getSparkleFuzzy(itemName: String): String? {
        sparkleMap[itemName]?.let { return it }
        return sparkleMap.entries.firstOrNull { (key, _) ->
            itemName.contains(key) || key.contains(itemName)
        }?.value
    }

    /**
     * 获取某个身份路径下所有物品的闪光点列表
     */
    fun getSparklesForPath(pathId: String): List<Pair<String, String>> {
        val path = LifePathData.paths.find { it.id == pathId } ?: return emptyList()
        return path.items.mapNotNull { item ->
            sparkleMap[item.name]?.let { item.name to it }
        }
    }

    /**
     * 所有收录的物品名称列表
     */
    fun allItemNames(): Set<String> = sparkleMap.keys
