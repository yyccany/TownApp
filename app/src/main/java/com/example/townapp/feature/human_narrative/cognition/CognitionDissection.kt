/**
 * 认知觉醒模块 - 认知手术刀数据模型
 * 
 * 本模块旨在用「真实的后果」解剖那些从小被灌输的传统规训
 * 不教用户变坏，也不教用户变好，只教用户「实事求是」
 */

// ============================================
// 认知解剖类型定义
// ============================================

/**
 * 认知解剖主题
 */
enum class CognitionTopic(val id: String, val displayName: String, val description: String) {
    OPENNESS("openness", "无话不说", "对父母、兄弟姐妹、朋友要无话不说，要坦诚相待"),
    HELPFULNESS("helpfulness", "乐于助人", "别人找你帮忙，你一定要帮，不然就是自私、就是冷漠"),
    TOLERANCE("tolerance", "吃亏是福", "吃亏是福，吃点亏没关系，别人会记得你的好"),
    DUAL_MORALITY("dual_morality", "双生存法则", "不管有钱没钱，做人的道理都是一样的，道德是通用的"),
    EDUCATION_DISCIPLINE("education_discipline", "教育规训", "读书考试、考公进体制，这套教育模式从哪来，为什么让你又焦虑又迷茫"),
    INTERGENERATIONAL_VERBAL_ABUSE("intergenerational_verbal_abuse", "代际语言伤害", "父母的吼叫、贬低、冷暴力从哪来，为什么那些话会钉在心里这么多年")
}

/**
 * 社会阶层
 */
enum class SocialClass(val id: String, val displayName: String, val income: String) {
    BOTTOM("bottom", "底层", "年收入 < 10万"),
    MIDDLE("middle", "中层", "年收入 10-100万"),
    TOP("top", "上层", "年收入 > 100万")
}

/**
 * 选择类型
 */
enum class ChoiceType(val id: String, val displayName: String) {
    TRADITIONAL("traditional", "传统道德选择"),
    REALISTIC("realistic", "现实法则选择")
}

/**
 * 后果类型
 */
enum class ConsequenceType {
    POSITIVE,   // 正面后果
    NEGATIVE,   // 负面后果
    NEUTRAL     // 中性后果
}

/**
 * 影响维度
 */
enum class ImpactDimension(val displayName: String) {
    MONEY("💰 经济影响"),
    RELATIONSHIP("👥 人际关系"),
    REPUTATION("⭐ 社会评价"),
    MENTAL("🧠 心理状态"),
    TIME("⏰ 时间和精力")
}

// ============================================
// 认知解剖数据类
// ============================================

/**
 * 认知解剖内容
 */
data class CognitionDissection(
    val topic: CognitionTopic,
    val title: String,
    val traditionalTeaching: String,           // 传统规训
    val coreQuestion: String,                 // 核心问题
    val traditionalReality: String,           // 传统规训的现实
    val realConsequence: String,             // 真实后果
    val analysis: String,                    // 分析
    val conclusion: String,                   // 结论
    val questions: List<String>,             // 思考问题
    val tags: List<String>                  // 标签
)

/**
 * 模拟器场景
 */
data class SimulatorScenario(
    val id: String,
    val title: String,                        // 场景标题
    val description: String,                  // 场景描述
    val traditionalChoice: ScenarioChoice,   // 传统道德选择
    val realisticChoice: ScenarioChoice,      // 现实法则选择
    val applicableClasses: List<SocialClass> // 适用的社会阶层
)

/**
 * 场景选择
 */
data class ScenarioChoice(
    val id: String,
    val title: String,                        // 选择标题
    val description: String,                 // 选择描述
    val consequences: Map<SocialClass, ChoiceConsequence>  // 不同阶层的后果
)

/**
 * 选择后果
 */
data class ChoiceConsequence(
    val immediateResult: String,             // 即时结果
    val longTermResult: String,              // 长期结果
    val impacts: List<ImpactResult>,        // 影响维度
    val quote: String,                      // 引用语
    val keyInsight: String                   // 关键洞察
)

/**
 * 影响结果
 */
data class ImpactResult(
    val dimension: ImpactDimension,
    val result: String,
    val magnitude: Int  // 1-5，影响程度
)

