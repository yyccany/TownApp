    fun getFoodsByCategory(category: String): List<CuisineFood> {
        return allFoods.filter { it.category == category }
    }

    fun getHealthyFoods(): List<CuisineFood> {
        return allFoods.filter { it.healthRisk == "LOW" || it.nutritionScore >= 35 }
    }

    fun getRiskyFoods(): List<CuisineFood> {
        return allFoods.filter { it.healthRisk == "HIGH" || it.healthRisk == "EXTREME" }
    }

    fun getFoodById(id: Int): CuisineFood? {
        return allFoods.find { it.id == id }
    }

    fun searchFoods(query: String): List<CuisineFood> {
        val lowerQuery = query.lowercase()
        return allFoods.filter {
            it.name.lowercase().contains(lowerQuery) ||
            it.category.lowercase().contains(lowerQuery) ||
            it.description.lowercase().contains(lowerQuery)
        }
    }

    fun getCategories(): List<String> {
        return allFoods.map { it.category }.distinct()
    }

    // 新增：按价格范围筛选
    fun getFoodsByPriceRange(minPrice: Double, maxPrice: Double): List<CuisineFood> {
        return allFoods.filter { it.price >= minPrice && it.price <= maxPrice }
    }

    // 新增：按卡路里范围筛选
    fun getFoodsByCaloriesRange(maxCalories: Double): List<CuisineFood> {
        return allFoods.filter { it.calories <= maxCalories }
    }

    // 新增：高蛋白食物
    fun getHighProteinFoods(minProtein: Double): List<CuisineFood> {
        return allFoods.filter { it.protein >= minProtein }
    }

