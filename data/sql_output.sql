-- ============================================
-- 1. 创建表结构
-- ============================================

-- 1.1 原材料表
CREATE TABLE IF NOT EXISTS materials (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    unit TEXT NOT NULL,
    price_per_unit REAL NOT NULL DEFAULT 0.0,
    contact_type TEXT NOT NULL DEFAULT 'non_contact' CHECK(contact_type IN ('edible', 'skin_contact', 'non_contact')),
    color_id INTEGER NOT NULL DEFAULT 5,
    description TEXT DEFAULT ''
);

-- 1.2 成品表
CREATE TABLE IF NOT EXISTS products (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    contact_type TEXT NOT NULL DEFAULT 'non_contact' CHECK(contact_type IN ('edible', 'skin_contact', 'non_contact')),
    color_id INTEGER NOT NULL DEFAULT 2,
    shape_type TEXT NOT NULL DEFAULT 'square' CHECK(shape_type IN ('square', 'horizontal', 'vertical', 'dish_plate', 'dish_bowl')),
    description TEXT DEFAULT ''
);

-- 1.3 成品-原材料组成关系表
CREATE TABLE IF NOT EXISTS compositions (
    product_id INTEGER NOT NULL,
    material_id INTEGER NOT NULL,
    use_amount REAL NOT NULL,
    unit TEXT NOT NULL,
    PRIMARY KEY (product_id, material_id),
    FOREIGN KEY (product_id) REFERENCES products(id),
    FOREIGN KEY (material_id) REFERENCES materials(id)
);

-- 1.4 食材/食品表
CREATE TABLE IF NOT EXISTS food_items (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    price_per_100g REAL NOT NULL DEFAULT 0.0,
    contact_type TEXT NOT NULL DEFAULT 'edible' CHECK(contact_type IN ('edible', 'skin_contact', 'non_contact')),
    nutritional_score REAL NOT NULL DEFAULT 75.0,
    serving_size INTEGER NOT NULL DEFAULT 100,
    protein_per_100g REAL NOT NULL DEFAULT 0.0,
    fat_per_100g REAL NOT NULL DEFAULT 0.0,
    carbohydrate_per_100g REAL NOT NULL DEFAULT 0.0,
    fiber_per_100g REAL NOT NULL DEFAULT 0.0,
    calories_per_100g REAL NOT NULL DEFAULT 0.0,
    typical_serving INTEGER NOT NULL DEFAULT 100,
    shelf_life_days REAL NOT NULL DEFAULT 30.0,
    meals_per_week INTEGER NOT NULL DEFAULT 3,
    quality_score REAL NOT NULL DEFAULT 0.5,
    breeding_degradation REAL NOT NULL DEFAULT 0.2,
    processing_loss REAL NOT NULL DEFAULT 0.1,
    note TEXT DEFAULT ''
);

-- 1.5 服饰单品表
CREATE TABLE IF NOT EXISTS clothing_items (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    brand TEXT DEFAULT '',
    price REAL NOT NULL DEFAULT 0.0,
    contact_type TEXT NOT NULL DEFAULT 'skin_contact' CHECK(contact_type IN ('edible', 'skin_contact', 'non_contact')),
    color TEXT DEFAULT '',
    size TEXT DEFAULT '',
    expected_lifespan_months REAL NOT NULL DEFAULT 24.0,
    max_wears_per_year INTEGER NOT NULL DEFAULT 60,
    note TEXT DEFAULT ''
);

-- 1.6 日用品/家居物品表
CREATE TABLE IF NOT EXISTS household_items (
    id INTEGER PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    price REAL NOT NULL DEFAULT 0.0,
    contact_type TEXT NOT NULL DEFAULT 'non_contact' CHECK(contact_type IN ('edible', 'skin_contact', 'non_contact')),
    lifespan_years REAL NOT NULL DEFAULT 5.0,
    brand TEXT DEFAULT '',
    note TEXT DEFAULT ''
);

-- 1.7 菜谱表
CREATE TABLE IF NOT EXISTS recipes (
    recipe_id INTEGER PRIMARY KEY,
    dish_name TEXT NOT NULL,
    cuisine TEXT DEFAULT '',
    simple_steps TEXT DEFAULT ''
);

-- 1.8 菜谱-食材关联表
CREATE TABLE IF NOT EXISTS recipe_ingredients (
    recipe_id INTEGER NOT NULL,
    food_id INTEGER NOT NULL,
    use_gram REAL NOT NULL DEFAULT 0.0,
    PRIMARY KEY (recipe_id, food_id),
    FOREIGN KEY (recipe_id) REFERENCES recipes(recipe_id),
    FOREIGN KEY (food_id) REFERENCES food_items(id)
);

